// Function to get planet data by name
export function getPlanetData(planetName) {
    const normalizedName = planetName.toLowerCase();
    return planetDataDetailed[normalizedName] || null;
}

// Function to get all planet names
export function getAllPlanetNames() {
    return Object.keys(planetDataDetailed);
}

// Function to search planets by criteria
export function searchPlanets(criteria) {
    return Object.entries(planetDataDetailed).filter(([name, data]) => {
        return name.toLowerCase().includes(criteria.toLowerCase()) ||
               data.name.toLowerCase().includes(criteria.toLowerCase()) ||
               data.type.toLowerCase().includes(criteria.toLowerCase());
    });
}

// Function to get planets by type
export function getPlanetsByType(type) {
    return Object.entries(planetDataDetailed).filter(([name, data]) => 
        data.type.toLowerCase() === type.toLowerCase()
    );
}

// Comparison functions
export function comparePlanetSizes() {
    return Object.entries(planetDataDetailed)
        .map(([name, data]) => ({
            name: data.name,
            radius: parseFloat(data.radius.replace(/[^\d.]/g, ''))
        }))
        .sort((a, b) => b.radius - a.radius);
}

export function comparePlanetDistances() {
    return Object.entries(planetDataDetailed)
        .map(([name, data]) => ({
            name: data.name,
            distance: parseFloat(data.distance.replace(/[^\d.]/g, ''))
        }))
        .filter(planet => planet.distance > 0)
        .sort((a, b) => a.distance - b.distance);
}

// Educational facts generator
export function getRandomFact(planetName = null) {
    if (planetName) {
        const planet = getPlanetData(planetName);
        if (planet && planet.facts.length > 0) {
            return planet.facts[Math.floor(Math.random() * planet.facts.length)];
        }
    }
    
    // Return random fact from any planet
    const allPlanets = Object.values(planetDataDetailed);
    const allFacts = allPlanets.flatMap(planet => planet.facts);
    return allFacts[Math.floor(Math.random() * allFacts.length)];
}

// Educational content for different age groups
export function getEducationalContent(planetName, ageGroup = 'general') {
    const planet = getPlanetData(planetName);
    if (!planet) return null;

    const content = {
        kids: {
            description: simplifyDescription(planet.description),
            funFacts: planet.facts.slice(0, 3),
            activity: generateActivity(planet)
        },
        teens: {
            description: planet.description,
            facts: planet.facts,
            science: generateScienceContent(planet)
        },
        general: {
            description: planet.description,
            facts: planet.facts,
            composition: planet.composition,
            discovery: {
                date: planet.discoveryDate,
                namedAfter: planet.namedAfter
            },
            interestingFact: planet.interestingFact
        }
    };

    return content[ageGroup] || content.general;
}

function simplifyDescription(description) {
    // Simplified version for kids
    const simplified = description
        .replace(/terrestrial/gi, 'rocky')
        .replace(/gas giant/gi, 'gas ball')
        .replace(/astronomical/gi, 'space')
        .split('.')[0]; // Take first sentence
    
    return simplified + '.';
}

